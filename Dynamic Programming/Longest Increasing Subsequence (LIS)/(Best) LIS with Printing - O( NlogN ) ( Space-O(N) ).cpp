int a[1000001];

// LIS() returns the vector containing LIS of the array a[] of size n
vector<int> LIS(int n) {

    vector<int> result;
    result.pb(a[0]);

    for (int i = 1; i < n; i++) {

        // if a[i] is greater than the last element of LIS, then include it also in the LIS
        if (result.back() < a[i])
            result.pb(a[i]);
        else {

            // finding the index of the element just greater than or equal to a[i]
            // and replacing it with a[i]
            int ind = lower_bound(all(result), a[i]) - result.begin();
            result[ind] = a[i];
        }
    }

    return result;
}

